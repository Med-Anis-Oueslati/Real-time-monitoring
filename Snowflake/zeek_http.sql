CREATE OR REPLACE TABLE ZEEK_HTTP (
    YEAR INTEGER,
    MONTH INTEGER,
    DAY INTEGER,
    HOUR INTEGER,
    MINUTE INTEGER,
    SECOND INTEGER,
    TIMESTAMP TIMESTAMP_NTZ,
    UID STRING,
    ID_ORIG_H STRING,
    ID_ORIG_P INTEGER,
    ID_RESP_H STRING,
    ID_RESP_P INTEGER,
    TRANS_DEPTH INTEGER,
    METHOD STRING,
    HOST STRING,
    URI STRING,
    VERSION STRING,
    USER_AGENT STRING,
    REQUEST_BODY_LEN INTEGER,
    RESPONSE_BODY_LEN INTEGER,
    STATUS_CODE INTEGER,
    STATUS_MSG STRING,
    TAGS ARRAY,
    RESP_FUIDS ARRAY,
    ORIG_FUIDS ARRAY,
    ORIG_MIME_TYPES ARRAY,
    RESP_MIME_TYPES ARRAY,
    HOSTNAME STRING,
    VM_ID STRING
)
CLUSTER BY (YEAR, MONTH, DAY);