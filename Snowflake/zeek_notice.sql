CREATE OR REPLACE TABLE ZEEK_NOTICE (
    YEAR INTEGER,
    MONTH INTEGER,
    DAY INTEGER,
    HOUR INTEGER,
    MINUTE INTEGER,
    SECOND INTEGER,
    TIMESTAMP TIMESTAMP_NTZ,
    UID STRING,
    ID_ORIG_H STRING,
    ID_ORIG_P INTEGER,
    ID_RESP_H STRING,
    ID_RESP_P INTEGER,
    PROTO STRING,
    NOTE STRING,
    MSG STRING,
    SRC STRING,
    DST STRING,
    P INTEGER,
    ACTIONS ARRAY,
    EMAIL_DEST ARRAY,
    SUPPRESS_FOR FLOAT,
    HOSTNAME STRING,
    VM_ID STRING
)
CLUSTER BY (YEAR, MONTH, DAY);